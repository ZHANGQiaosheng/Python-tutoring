# 获取当前时间并美化显示
import datetime
现在 = datetime.datetime.now()
年 = 现在.year
月 = 现在.month  
日 = 现在.day
小时 = 现在.hour
分钟 = 现在.minute

print(f"📅 今天是{年}年{月}月{日}日")
print(f"🕐 现在是{小时:02d}:{分钟:02d}")

星期名字 = ["星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日"]
今天星期几 = 现在.weekday()
print(f"🌟 今天是{星期名字[今天星期几]}")

生日月份 = 6
生日日期 = 15
if 月 < 生日月份 or (月 == 生日月份 and 日 < 生日日期):
    距离生日 = (datetime.date(年, 生日月份, 生日日期) - datetime.date(年, 月, 日)).days
else:
    距离生日 = (datetime.date(年+1, 生日月份, 生日日期) - datetime.date(年, 月, 日)).days

print(f"🎂 距离我的生日还有{距离生日}天")
print()

