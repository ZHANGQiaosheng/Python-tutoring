# 练习3.3 - 时间计算器
print("练习3.3：时间魔法挑战")
import datetime

当前时间 = datetime.datetime.now()
生日月 = 8
生日日 = 15
开学月 = 9  
开学日 = 1

print("👦 学生任务：计算重要日期的倒计时")
# 答案：
年 = 当前时间.year
月 = 当前时间.month
日 = 当前时间.day

# 计算到生日的天数
if 月 < 生日月 or (月 == 生日月 and 日 < 生日日):
    到生日 = (datetime.date(年, 生日月, 生日日) - datetime.date(年, 月, 日)).days
else:
    到生日 = (datetime.date(年+1, 生日月, 生日日) - datetime.date(年, 月, 日)).days

# 计算到开学的天数
if 月 < 开学月 or (月 == 开学月 and 日 < 开学日):
    到开学 = (datetime.date(年, 开学月, 开学日) - datetime.date(年, 月, 日)).days
else:
    到开学 = (datetime.date(年+1, 开学月, 开学日) - datetime.date(年, 月, 日)).days

print("⏰ 重要日期倒计时")
print("=" * 20)
print(f"今天是：{年}年{月}月{日}日")
print(f"距离生日还有：{到生日}天 🎂")
print(f"距离开学还有：{到开学}天 🏫")

星期几 = ["星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日"]
今天星期 = 当前时间.weekday()
print(f"今天是：{星期几[今天星期]} {'📚工作日' if 今天星期 < 5 else '🎉周末'}")
print("=" * 20)
